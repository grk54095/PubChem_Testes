public class soma {
    public static void main(String[] args) {
        sum(10, 5);
    }

    static void sum(int x, int y) {
       // sum nome da função
       //() é usado para usarmos para definir o que chegara na função
       //{} define o corpo da função
        System.out.println(x+y); 
        System.out.println(x-y);
        System.out.println(x/y);
        System.out.println(x*y);

    }
}
